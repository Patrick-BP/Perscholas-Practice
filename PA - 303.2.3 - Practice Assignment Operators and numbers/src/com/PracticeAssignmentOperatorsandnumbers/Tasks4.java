public class Tasks4 {
    public static void main(String[] args) {
        // Preceding values
        int x = 7; // Binary: 101
        int y = 17; // Binary: 011

        // Bitwise OR calculation
        int z = x | y;

        // Printing the result
        System.out.println("Bitwise OR between " + x + " and " + y + " is: " + z);


    }
}
